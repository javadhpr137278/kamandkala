    document.addEventListener('DOMContentLoaded', function() {
        // تابع بروزرسانی تعداد
        function updateQuantity(cartId, quantity) {
            fetch(`/cart/update/${cartId}`, {
                method: 'PUT',
                headers: {
                    'Content-Type': 'application/json',
                    'X-CSRF-TOKEN': '{{ csrf_token() }}',
                    'Accept': 'application/json'
                },
                body: JSON.stringify({ quantity: quantity })
            })
                .then(response => response.json())
                .then(data => {
                    if(data.success) {
                        location.reload();
                    } else {
                        alert(data.message || 'خطا در بروزرسانی');
                        location.reload();
                    }
                })
                .catch(error => {
                    console.error('Error:', error);
                    alert('خطا در ارتباط با سرور');
                });
        }

        // دکمه +
        document.querySelectorAll('.cart-counter-plus').forEach(btn => {
            btn.addEventListener('click', function(e) {
                e.preventDefault();
                let id = this.getAttribute('data-id');
                let input = document.querySelector(`.cart-quantity-input[data-id="${id}"]`);
                if (input) {
                    let newVal = parseInt(input.value) + 1;
                    let max = parseInt(input.getAttribute('max'));
                    if (newVal <= max) {
                        updateQuantity(id, newVal);
                    } else {
                        alert('تعداد وارد شده بیشتر از موجودی انبار است');
                    }
                }
            });
        });

        // دکمه -
        document.querySelectorAll('.cart-counter-mines').forEach(btn => {
            btn.addEventListener('click', function(e) {
                e.preventDefault();
                let id = this.getAttribute('data-id');
                let input = document.querySelector(`.cart-quantity-input[data-id="${id}"]`);
                if (input) {
                    let newVal = parseInt(input.value) - 1;
                    if (newVal >= 1) {
                        updateQuantity(id, newVal);
                    }
                }
            });
        });

        // تغییر دستی تعداد
        document.querySelectorAll('.cart-quantity-input').forEach(input => {
            input.addEventListener('change', function() {
                let id = this.getAttribute('data-id');
                let newVal = parseInt(this.value);
                let max = parseInt(this.getAttribute('max'));
                let min = parseInt(this.getAttribute('min'));

                if (isNaN(newVal)) {
                    this.value = 1;
                    newVal = 1;
                }

                if (newVal > max) {
                    alert('تعداد وارد شده بیشتر از موجودی انبار است');
                    this.value = max;
                    newVal = max;
                }

                if (newVal < min) {
                    this.value = min;
                    newVal = min;
                }

                if (newVal !== parseInt(this.defaultValue)) {
                    updateQuantity(id, newVal);
                }
            });
        });

        // حذف از سبد خرید
        document.querySelectorAll('.remove-from-cart').forEach(btn => {
